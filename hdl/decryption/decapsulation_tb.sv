/*
    goal: compare ss_mem from c code to ss from verilog
    step:
        1. use readmemh to load hex value from
            input: ck_mem,sk_mem
            output: ss_mem
        generated by official c code
        readmemh loads a memory -> 8 bits per entry
        2. load 
            ct_mem -> decapsulation's ct
            sk_mem -> decapsulation's sk
            ss_mem -> expected_ss to compare with decapsulation's ss
        3. see if decapsulation's ss matches expected_ss
        4. it fails, f = 0.
            that means ct_prime != ct
            the reencrypted message didn't match the original
            4.1 check if m_prime from c code = m_prime in verilog
                if not equal, bug is before post_decryption 
                result: m_prime is equal, the bug is in post decryption
            4.2 check (c',pre−k') = SHA3−512(pre−k,m')
            and Ct'=IND−CPA−KyberEncryption (m',PK,c')
                see if c',pre−k', Ct'
                result: c' and pre-k' match, ct' doesn't
                this means the bug is in re-encryption
            4.4 check PK into re-encryption
                result: PK matches C, the problem is in re-encryption
            4.5 check msg_poly in pre_encryption (4. Decode decompress msg) in encryption_top
                result: normal
            4.6 check r,e1,e2 to check noise generation in pre_encryption(7. Noise Generation)
                result: matches with C result
                now we must test 
                1. decode_pk -> t_vec / rho
                2. public_matrix_gen -> A^T
                3. main_computation -> x/y
                4. add/reduce/compress
*/
